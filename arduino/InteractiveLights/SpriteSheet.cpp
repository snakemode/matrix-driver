sprited_character sprite_sheet::spriteDataFor(int requestedCharacter)
{
  char asChar = requestedCharacter; // String.fromCharCode(requestedCharacter);

  if (!supportsSprite(asChar))
  {
    sprited_character result = {{0, 0, 0, 0, 0, 0, 0, 0}, 1};
    return result;
  }

  auto index = indexOfSprite(asChar);

  auto height = 8; // read from cfg
  auto width = spriteSheet[index + 1];
  auto recordLength = width * height;
  auto offset = index + 2;

  sprited_character result;
  result.width = width;

  for (auto position = 0; position < recordLength; position++)
  {
    auto values = spriteSheet[offset + position];
    result.spriteData[position] = values;
  }

  return result;
}

int sprite_sheet::getTotalWidthOfText(const char *bytes, int length)
{
  auto textStart = search_utils::indexOf(bytes, length, STX_StartOfText) + 1;
  auto textEnd = search_utils::indexOf(bytes, length, ETX_EndOfText);

  auto total = 0;

  for (auto i = textStart; i < textEnd; i++)
  {
    auto asciiCode = bytes[i];
    char character = asciiCode; // String.fromCharCode(asciiCode);

    if (!sprite_sheet::supportsSprite(character))
    {
      total += 2;
      continue;
    }

    auto index = sprite_sheet::indexOfSprite(character);
    auto widthOfSpriteIndex = index + 1;
    auto width = (int)spriteSheet[widthOfSpriteIndex];

    total += 1;
    total += width;
  }

  return total;
}

bool sprite_sheet::supportsSprite(char character)
{
  return sprite_sheet::indexOfSprite(character) > -1;
}

